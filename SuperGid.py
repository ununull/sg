class QuizApp:
    def __init__(self, root):
        self.root = root
        self.root.title("СУПЕРГИД")
        self.root.attributes('-fullscreen', True)

        self.font_normal = tkfont.Font(family="Arial", size=16, weight="normal", underline=0)
        self.font_bold = tkfont.Font(family="Arial", size=16, weight="bold", underline=0)

        self.btn_quit = tk.Button(root, text="ЗАКРЫТЬ", command=self.root.destroy,
                                  font=("Arial", 16), fg="black", bg="#e5e5e5", bd=0)
        self.btn_quit.place(relx=1.0, x=-10, y=10, anchor='ne')
        self.btn_quit.bind("<Enter>", lambda e: self.btn_quit.config(fg="red", bg="#d5d5d5"))
        self.btn_quit.bind("<Leave>", lambda e: self.btn_quit.config(fg="black", bg="#e5e5e5"))

        self.q_index = 0
        self.score = 0
        self.answered = False

        self.lbl_progress = tk.Label(root, font=("Arial", 16))
        self.lbl_progress.pack(pady=20)

        self.lbl_question = tk.Label(root, wraplength=self.root.winfo_screenwidth() - 100,
                                     font=("Arial", 18))
        self.lbl_question.pack(pady=30)

        self.option_labels = []

        self.lbl_remaining = tk.Label(root, font=("Arial", 14), fg="red")

        self.btn_next = tk.Button(root, text="СЛЕДУЮЩИЙ", command=self.next_question,
                                  font=("Arial", 16), width=20, height=2, bg="#e5e5e5", bd=0)
        self.btn_next.pack(pady=40)
        self.btn_next.pack_forget()
        self.btn_next.bind("<Enter>", lambda e: self.btn_next.config(bg="#d5d5d5"))
        self.btn_next.bind("<Leave>", lambda e: self.btn_next.config(bg="#e5e5e5"))

        self.result_frame = tk.Frame(root)
        self.lbl_result = tk.Label(self.result_frame, font=("Arial", 18), fg="black")
        self.lbl_result.pack(pady=(0, 20))
        self.btn_restart = tk.Button(self.result_frame, text="НАЧАТЬ ЗАНОВО",
                                     command=self.restart_quiz, font=("Arial", 16), width=20, height=2,
                                     bg="#e5e5e5", bd=0)
        self.btn_restart.pack()
        self.btn_restart.bind("<Enter>", lambda e: self.btn_restart.config(bg="#d5d5d5"))
        self.btn_restart.bind("<Leave>", lambda e: self.btn_restart.config(bg="#e5e5e5"))
        self.result_frame.pack_forget()

        self.shuffled_options = []
        self.correct_indices = set()
        self.selected_indices = set()
        self.max_selections = 1

        self.root.bind("<Configure>", lambda e: self.position_lbl_remaining())

        self.load_question()

    def position_lbl_remaining(self):
        self.root.update_idletasks()
        win_width = self.root.winfo_width()
        win_height = self.root.winfo_height()
        lbl_width = self.lbl_remaining.winfo_reqwidth()
        lbl_height = self.lbl_remaining.winfo_reqheight()
        x = (win_width - lbl_width) // 2
        y = win_height - lbl_height - 20
        self.lbl_remaining.place(x=x, y=y)

    def clean_text(self, text):
        return text[1:] if text.startswith("+") else text

    def load_question(self):
        self.answered = False
        self.btn_next.pack_forget()
        self.result_frame.pack_forget()
        self.selected_indices.clear()

        q = questions[self.q_index]

        self.correct_indices = {i for i, opt in enumerate(q['options']) if opt.startswith("+")}
        self.max_selections = len(self.correct_indices)

        options_with_idx = list(enumerate(q['options']))
        random.shuffle(options_with_idx)

        self.shuffled_options = [opt for idx, opt in options_with_idx]

        new_correct_indices = set()
        for new_idx, (orig_idx, opt) in enumerate(options_with_idx):
            if orig_idx in self.correct_indices:
                new_correct_indices.add(new_idx)
        self.correct_indices = new_correct_indices

        self.lbl_progress.config(text=f"Вопрос {self.q_index + 1} из {len(questions)}")
        self.lbl_question.config(text=q['question'])

        if self.max_selections > 1:
            self.lbl_remaining.config(text=f"Выберите несколько ответов: {self.max_selections} шт.")
        else:
            self.lbl_remaining.config(text="")

        self.position_lbl_remaining()

        for lbl in self.option_labels:
            lbl.destroy()
        self.option_labels.clear()

        wrap_len = self.root.winfo_screenwidth() - 250

        for i, option_text in enumerate(self.shuffled_options):
            clean_opt = self.clean_text(option_text)
            lbl = tk.Label(self.root, text=clean_opt, font=self.font_normal, fg="black",
                           cursor="hand2", anchor="w", justify="left", bd=0, relief="flat",
                           wraplength=wrap_len)
            lbl.bind("<Button-1>", lambda e, idx=i: self.select_answer(idx))
            lbl.bind("<Enter>", lambda e, lbl=lbl: self.on_enter(lbl))
            lbl.bind("<Leave>", lambda e, lbl=lbl: self.on_leave(lbl))
            lbl.pack(pady=10, fill='x', padx=100)
            self.option_labels.append(lbl)

        self.lbl_progress.pack(pady=20)
        self.lbl_question.pack(pady=30)
        self.btn_next.pack_forget()

    def select_answer(self, idx):
        if self.answered:
            return

        if self.max_selections > 1:
            if idx in self.selected_indices:
                self.selected_indices.remove(idx)
                self.option_labels[idx].config(bg=self.root.cget('bg'), relief="flat", bd=0, font=self.font_normal)
            else:
                if len(self.selected_indices) < self.max_selections:
                    self.selected_indices.add(idx)
                    self.option_labels[idx].config(bg="#cce5ff", relief="solid", bd=0, font=self.font_bold)
                else:
                    return

            remaining = self.max_selections - len(self.selected_indices)
            self.lbl_remaining.config(text=f"Осталось выбрать: {remaining}")
            self.position_lbl_remaining()

            if len(self.selected_indices) == self.max_selections:
                self.show_multiple_choice_result()
        else:
            self.answered = True

            correct = next(iter(self.correct_indices))

            for i, lbl in enumerate(self.option_labels):
                lbl.unbind("<Button-1>")
                lbl.unbind("<Enter>")
                lbl.unbind("<Leave>")
                if i == correct:
                    lbl.config(font=self.font_normal, bg="#d4edda", relief="solid", bd=0)
                elif i == idx:
                    lbl.config(font=self.font_normal, bg="#f8d7da", relief="solid", bd=0)
                else:
                    lbl.config(font=self.font_normal, bg=self.root.cget('bg'), relief="flat", bd=0)

            if idx == correct:
                self.score += 1

            self.lbl_remaining.config(text="")
            self.position_lbl_remaining()
            self.btn_next.pack()

    def show_multiple_choice_result(self):
        self.answered = True

        for lbl in self.option_labels:
            lbl.unbind("<Button-1>")
            lbl.unbind("<Enter>")
            lbl.unbind("<Leave>")

        correct_bg = "#d4edda"
        incorrect_bg = "#f8d7da"
        default_bg = self.root.cget('bg')

        for i, lbl in enumerate(self.option_labels):
            if i in self.correct_indices:
                bg = correct_bg
            elif i in self.selected_indices:
                bg = incorrect_bg
            else:
                bg = default_bg

            font = self.font_bold if i in self.selected_indices else self.font_normal

            lbl.config(bg=bg, font=font, relief="solid" if bg != default_bg else "flat", bd=0)

        if self.selected_indices == self.correct_indices:
            self.score += 1

        self.lbl_remaining.config(text="")
        self.position_lbl_remaining()
        self.btn_next.pack()

    def on_enter(self, lbl):
        if not self.answered:
            if lbl.cget('bg') != "#cce5ff":
                lbl.config(bg="#cce5ff", relief="solid", bd=0)

    def on_leave(self, lbl):
        if not self.answered:
            if lbl.cget('bg') == "#cce5ff":
                lbl.config(bg=self.root.cget('bg'), relief="flat", bd=0)

    def next_question(self):
        self.q_index += 1
        if self.q_index == len(questions):
            self.btn_next.pack_forget()
            self.lbl_question.pack_forget()
            self.lbl_progress.pack_forget()
            self.lbl_remaining.place_forget()
            for lbl in self.option_labels:
                lbl.pack_forget()
                lbl.unbind("<Button-1>")
                lbl.unbind("<Enter>")
                lbl.unbind("<Leave>")

            self.lbl_result.config(text=f"Правильных ответов: {self.score} из {len(questions)}")
            self.result_frame.pack(expand=True)
        else:
            self.load_question()
            self.btn_next.config(state=tk.NORMAL)

    def restart_quiz(self):
        self.q_index = 0
        self.score = 0
        self.btn_next.config(state=tk.NORMAL)

        self.result_frame.pack_forget()

        self.lbl_progress.pack(pady=20)
        self.lbl_question.pack(pady=30)

        self.load_question()

if __name__ == "__main__":
    root = tk.Tk()
    app = QuizApp(root)
    root.mainloop()
